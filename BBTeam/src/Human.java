
public class Human {
	
	private int no;
	private String name;
	private int age;
	private int height;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	
	public Human(int no, String name, int age, int height) {
		this.no = no;
		this.name = name;
		this.age = age;
		this.height = height;
	}
	

}
